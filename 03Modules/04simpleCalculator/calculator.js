var add = function(num1, num2){
  return num1 + num2;
};

var multiply = function(num1, num2){
  return num1 * num2;
};

var devide = function(num1, num2){
  return num1 / num2;
};

var subtract = function(num1, num2){
  return num1 - num2;
};

exports.add = add;
exports.multiply = multiply;
exports.devide = devide;
exports.subtract = subtract;
